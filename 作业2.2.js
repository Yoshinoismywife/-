function add(a)
{
    switch(a)
    {
        case 1:
            console.log(2);
            break;
        case 2:
            console.log(3);
            break;
        case 3:
            console.log(4);
            break;
        case 4:
            console.log(5);
            break;
    }

}

for(var i = 1; i < 5; i++)
{
    add(i)
}